package ch04_control;

public class Test08_for {

	public static void main(String[] args) {
		//1~10까지 출력
		 for (int i=1 ;i<=10;i++){
			 System.out.print(i+" ");
		 }
		 System.out.println();
		//==================================
		 for(int i=10;i>0;i--){
			 System.out.print(i+" ");
		 }
		 System.out.println();

	}

}
