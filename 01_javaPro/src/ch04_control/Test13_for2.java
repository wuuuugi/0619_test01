package ch04_control;

public class Test13_for2 {
	public static void main(String[] args) {
		for (int i =1; i<=4 ; i++){
			for(int j =1; j<=4; j++){
				System.out.println("i:"+i+"  "+j+" ");
			}
			System.out.println();
			
		}
		for (int i =1; i<=4 ; i++){
			for(int j =1; j<=4; j++){
				System.out.print("*");
			}
			System.out.println();

			
		}
	}

}
