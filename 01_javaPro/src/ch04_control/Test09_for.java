package ch04_control;

public class Test09_for {

	public static void main(String[] args) {
		//수요일 5번 출력
		for(int i =0 ; i <5; i++){
			System.out.println("수요일");
		}
		System.out.println();
		for(int i =0 ; i <10; i++){
			System.out.print(i+1+"손명욱 " );
		}
		System.out.println();

		
	}

}
