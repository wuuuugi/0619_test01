package ch04_control;

public class Test17_while {
	public static void main(String[] args) {
		//while문 : 1 2 3 ,,9 10까지 출력
		int i=1;
		while(i<11){
			System.out.println(i);
			i++;
		}
		System.out.println();
		i = 10;
		do{
			System.out.println(i);
			i++;
		}while(i<21);
			
		
	}

}
